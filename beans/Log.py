class Log:

    def __init__(self,
                 date=None,
                 user_id=None,
                 first_name=None,
                 last_name=None,
                 username=None,
                 lang_code=None,
                 command=None,
                 text=None):

        self.date = date
        self.user_id = user_id
        self.first_name = first_name
        self.last_name = last_name
        self.username = username
        self.lang_code = lang_code
        self.command = command
        self.text = text
